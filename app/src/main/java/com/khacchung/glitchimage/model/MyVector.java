package com.khacchung.glitchimage.model;

public class MyVector {
    private int width;
    private int height;

    public MyVector(int width, int height) {
        this.height = height;
        this.width = width;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
